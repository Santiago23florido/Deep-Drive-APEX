export APEX_RECOGNITION_TRACKER_PLANNING_STATUS_TOPIC="${APEX_RECOGNITION_TRACKER_PLANNING_STATUS_TOPIC:-/apex/planning/fixed_map_status}"
export APEX_RECOGNITION_TRACKER_ODOM_TOPIC="${APEX_RECOGNITION_TRACKER_ODOM_TOPIC:-/apex/odometry/fixed_map_localized}"
