export APEX_FIXED_MAP_RECORDED_PATH_TOPIC="${APEX_FIXED_MAP_RECORDED_PATH_TOPIC:-/apex/fixed_map/route_preview_path}"
export APEX_FIXED_MAP_STATUS_TOPIC="${APEX_FIXED_MAP_STATUS_TOPIC:-/apex/fixed_map/status}"

