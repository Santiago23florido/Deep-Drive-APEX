#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
APEX_ROOT="$(cd "${SCRIPT_DIR}/../.." && pwd)"
REPO_ROOT="$(cd "${APEX_ROOT}/.." && pwd)"
OFFLINE_ROOT="${APEX_OFFLINE_REFINED_ROOT:-${APEX_ROOT}/.apex_runtime/offline_refined_maps}"
RUN_ARG="${1:-latest}"

to_container_path() {
  local host_path="$1"
  case "${host_path}" in
    "${REPO_ROOT}"/*)
      printf '/work/repo/%s\n' "${host_path#"${REPO_ROOT}/"}"
      ;;
    *)
      printf '%s\n' "${host_path}"
      ;;
  esac
}

if [[ "${RUN_ARG}" == "latest" ]]; then
  RUN_DIR="$(ls -1dt "${OFFLINE_ROOT}"/offline_refined_* 2>/dev/null | head -n 1 || true)"
elif [[ "${RUN_ARG}" = /* ]]; then
  RUN_DIR="${RUN_ARG}"
else
  RUN_DIR="${OFFLINE_ROOT%/}/${RUN_ARG}"
fi

if [[ -z "${RUN_DIR}" || ! -d "${RUN_DIR}" ]]; then
  echo "[APEX][ERROR] No offline_refined run found. Checked: ${RUN_DIR:-${OFFLINE_ROOT}}" >&2
  exit 1
fi

for required in map_points_xy.csv path_poses_xyyaw.csv poses.json; do
  if [[ ! -f "${RUN_DIR}/${required}" ]]; then
    echo "[APEX][ERROR] Missing ${RUN_DIR}/${required}" >&2
    exit 1
  fi
done

CONTAINER_RUN_DIR="$(to_container_path "$(cd "${RUN_DIR}" && pwd)")"
CONTAINER_FIXED_MAP_DIR="${CONTAINER_RUN_DIR%/}/fixed_map"

export APEX_ENABLE_KINEMATICS="${APEX_ENABLE_KINEMATICS:-1}"
export APEX_ENABLE_IMU_LIDAR_FUSION="${APEX_ENABLE_IMU_LIDAR_FUSION:-1}"
export APEX_ESTIMATION_BACKEND="${APEX_ESTIMATION_BACKEND:-fixed_map}"
