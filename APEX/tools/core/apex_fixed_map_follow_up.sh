export APEX_ENABLE_PATH_TRACKER="${APEX_ENABLE_PATH_TRACKER:-0}"
export APEX_ENABLE_RECOGNITION_TOUR_PLANNER="${APEX_ENABLE_RECOGNITION_TOUR_PLANNER:-0}"
export APEX_ENABLE_RECOGNITION_TOUR_TRACKER="${APEX_ENABLE_RECOGNITION_TOUR_TRACKER:-1}"
export APEX_ENABLE_FIXED_MAP_ROUTE_PLANNER="${APEX_ENABLE_FIXED_MAP_ROUTE_PLANNER:-1}"
export APEX_ENABLE_FIXED_MAP_PUBLISHER="${APEX_ENABLE_FIXED_MAP_PUBLISHER:-1}"
export APEX_ENABLE_CMDVEL_ACTUATION_BRIDGE="${APEX_ENABLE_CMDVEL_ACTUATION_BRIDGE:-1}"
export APEX_ENABLE_MANUAL_CONTROL_BRIDGE="${APEX_ENABLE_MANUAL_CONTROL_BRIDGE:-0}"
export APEX_ENABLE_RECOGNITION_SESSION_MANAGER="${APEX_ENABLE_RECOGNITION_SESSION_MANAGER:-0}"
export APEX_ENABLE_OFFLINE_SUBMAP_REFINER="${APEX_ENABLE_OFFLINE_SUBMAP_REFINER:-0}"

export APEX_FIXED_MAP_AUTOBUILD="${APEX_FIXED_MAP_AUTOBUILD:-1}"
export APEX_FIXED_MAP_RUN_DIR="${APEX_FIXED_MAP_RUN_DIR:-${CONTAINER_RUN_DIR}}"
export APEX_FIXED_MAP_DIR="${APEX_FIXED_MAP_DIR:-${CONTAINER_FIXED_MAP_DIR}}"
export APEX_FIXED_MAP_CLEARANCE_M="${APEX_FIXED_MAP_CLEARANCE_M:-0.15}"
export APEX_FIXED_MAP_FRAME_ID="${APEX_FIXED_MAP_FRAME_ID:-odom_imu_lidar_fused}"

export APEX_FUSION_ODOM_TOPIC="${APEX_FUSION_ODOM_TOPIC:-/apex/odometry/fixed_map_localized}"
export APEX_FUSION_PATH_TOPIC="${APEX_FUSION_PATH_TOPIC:-/apex/localization/fixed_map_path}"
export APEX_FUSION_POSE_TOPIC="${APEX_FUSION_POSE_TOPIC:-/apex/localization/fixed_map_pose}"
