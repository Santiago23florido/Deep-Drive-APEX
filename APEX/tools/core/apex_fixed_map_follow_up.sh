export APEX_FIXED_MAP_ROUTE_PATH_TOPIC="${APEX_FIXED_MAP_ROUTE_PATH_TOPIC:-/apex/planning/fixed_map_path}"
export APEX_FIXED_MAP_ROUTE_STATUS_TOPIC="${APEX_FIXED_MAP_ROUTE_STATUS_TOPIC:-/apex/planning/fixed_map_status}"
